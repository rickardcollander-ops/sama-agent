"""
KeywordOpportunityAgent — extracts current keyword targeting from a crawled
site and generates new keyword opportunities to grow organic + AI traffic.

Inputs (provided by SiteAuditAgent):
  - host / base_url: the audited domain.
  - pages: List[PageReport] from the site audit, including titles, H1/H2 text,
    meta descriptions, and a body_text_sample per page.
  - tenant_config: brand_name, competitors, audience, USP — used to bias the
    LLM toward queries the brand actually wants to rank for.

Outputs (a JSON-serialisable dict consumed by the dashboard):
  - current_keywords: phrases the site already targets, with the URLs that
    target them. Built from H1/H2/title/meta + a TF-IDF-ish body extractor.
  - opportunities: new keyword candidates with intent, suggested target URL,
    and rationale. Generated by Claude when available; deterministic fallback
    otherwise.
  - clusters: pillar + supporting keyword groups so the user can plan a
    content topic-cluster strategy instead of one-off blog posts.
  - competitor_gap: best-effort competitor SERP gap when ValueSERP is
    configured; otherwise omitted.
  - related_searches: ValueSERP `related_searches` for the top-extracted
    page topic (free signal of long-tail demand).
"""

from __future__ import annotations

import asyncio
import json
import logging
import re
from collections import Counter
from typing import Any, Dict, List, Optional, Tuple, TYPE_CHECKING
from urllib.parse import urlparse

import httpx

from shared.config import settings

if TYPE_CHECKING:  # avoid circular import at runtime
    from agents.site_audit import PageReport

logger = logging.getLogger(__name__)


